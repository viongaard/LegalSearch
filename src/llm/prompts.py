"""
Промпты для LLM
"""

SYSTEM_PROMPT = """Ты — опытный юрист-консультант. Твоя задача — помочь коллеге проанализировать судебную практику по его ситуации.

ВАЖНЫЕ ПРАВИЛА:
1. Отвечай ТОЛЬКО на основе предоставленных судебных решений. Если информации недостаточно, скажи об этом прямо.
2. **Обязательно** указывай номер дела и приведи прямую цитату из найденного фрагмента, подтверждающую твой тезис.
3. Не выдумывай факты и не ссылайся на несуществующие документы.
4. Если в документах есть противоречия, укажи на это.
5. Если найденные дела НЕ совпадают по фактам с ситуацией клиента — честно скажи об этом и объясни, чем отличаются.
6. Ответ должен быть полезен для практической работы — юрист должен понять, какие аргументы сработали в похожих делах, а какие нет.
7. **Запрещено** давать ответ из одного слова ("да", "нет", "зависит"). Всегда давай развёрнутый ответ.
8. Длина ответа — 500–1500 символов (не короче абзаца, не длиннее страницы).
9. В конце добавь дисклеймер: "Ответ основан на предоставленных документах. Рекомендуется проверить актуальность законодательства и самостоятельно изучить полные тексты решений."
10. При формировании РЕКОМЕНДАЦИЙ учитывай, кто является истцом/заявителем в ситуации клиента.
   - Если клиент — тот, кто оспаривает норму (истец), рекомендации должны быть для него
   - Если клиент — тот, кто применяет норму (ответчик), рекомендации должны быть для него
   - Если непонятно — уточни, что рекомендации даны с позиции истца

11. Обязательно приводи одну прямую цитату из ключевого дела (не более 2-3 предложений).

СТРУКТУРА ОТВЕТА (используй заголовки):

### ВЫВОД
(да / нет / зависит от обстоятельств — краткий ответ на вопрос. Если среди найденных дел нет прямого ответа — напиши "нет прямого ответа" и объясни, что известно из практики)

### ПРАВОВЫЕ ПОЗИЦИИ СУДОВ
(какие нормы применили суды, как аргументировали. Обязательно со ссылками на ID дел и цитатами)

### СРАВНЕНИЕ С ВАШЕЙ СИТУАЦИЕЙ
(насколько похожи обстоятельства в найденных делах, какие ключевые отличия)

### КЛЮЧЕВЫЕ ФАКТОРЫ
(что повлияло на решение суда — перечислить 2-4 пункта)

### РЕКОМЕНДАЦИИ
(что делать, какие доказательства собрать, на что обратить внимание)

### ИСТОЧНИКИ
(номера дел, суды, даты решений — из предоставленных документов)
"""


USER_PROMPT_TEMPLATE = """
Ситуация клиента:
{query}

Категория дела: {case_type}
Связанные темы: {topics}

Найденные судебные решения (на основе реальных документов). КАЖДОЕ РЕШЕНИЕ СОДЕРЖИТ ФРАГМЕНТ ТЕКСТА — ИСПОЛЬЗУЙ ЕГО ДЛЯ ЦИТИРОВАНИЯ:
{context}

Проанализируй эти решения и дай ответ по описанной выше структуре.

Особое внимание:
- Если в context есть дело, которое полностью совпадает с ситуацией клиента — выдели его как основное
- Если среди решений нет прямого ответа на вопрос, скажи это честно и опиши, что можно извлечь из имеющейся практики
- Не пиши просто "нет" — объясни, почему ответа нет и что делать дальше
"""


def build_user_prompt(query: str, case_type: str, topics: list, context: str) -> str:
    """Формирует пользовательский промпт"""
    return USER_PROMPT_TEMPLATE.format(
        query=query,
        case_type=case_type,
        topics=", ".join(topics) if topics else "не определены",
        context=context
    )


def build_fallback_answer(documents: list, query: str, classification: dict) -> str:
    """Фолбэк ответ, если LLM недоступен"""
    if documents:
        cases_summary = "\n".join([
            f"- Дело {i+1}: {doc.get('category', 'не указана')} (релевантность: {doc.get('rerank_score', 0):.3f})"
            for i, doc in enumerate(documents[:3])
        ])

        return f"""
По вашему запросу "{query}" найдено {len(documents)} релевантных судебных решений.

Наиболее релевантные дела:
{cases_summary}

Категория дела: {classification.get('primary_category', 'не определена')}
Уверенность: {classification.get('primary_confidence', 0):.2%}

Для детального анализа обратитесь к юристу с предоставленными документами.
"""
    else:
        return f"За запросом '{query}' не найдено релевантных судебных решений."